"""
Listas em Python podem armazenar de maneira sequencial qualquer tipo de objeto.
Pode-se criar listas utilizando: construtor list, função range ou colocando valores separados
por vírgula dentro de colchetes.
Listas são objetos multáveis
"""

frutas = ["laranja", "maca", "uva"]
print(frutas)

frutas = []
print(frutas)

letras = list("python")
print(letras)

numeros = list(range(10))
print(numeros)

carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True]
print(carro)
