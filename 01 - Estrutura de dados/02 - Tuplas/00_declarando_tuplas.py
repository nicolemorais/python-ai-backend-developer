"""
Tuplas são estruturas imutáveis e isso que a difere de listas.
Podemos criar tuplas através da classe tuple, ou colocando valores separados por vírgula entre parenteses.
"""

frutas = (
    "laranja",
    "pera",
    "uva",
)
print(frutas)

letras = tuple("python")
print(letras)

numeros = tuple([1, 2, 3, 4])
print(numeros)

pais = ("Brasil",)
print(pais)
